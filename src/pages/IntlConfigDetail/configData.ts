const intlStorageData = storageIntlConfigDataString ? JSON.parse(storageIntlConfigDataString) : [];
export const initDataSource =
  intlStorageData && intlStorageData.length > 0
    ? intlStorageData
    : intlConfigDataKeys.map((item) => ({
        key: count++,
        ...initDataItem,
        ...item,
      }));

export function generatJsonFiles(data: ItemProps[]) {
  const dataJson = JSON.stringify(data);
  const blob = new Blob([dataJson], { type: 'application/json' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');

  a.href = url;

  a.download = 'messages.json';

  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
}
